package week1.day2.Bonus;

public class LearnDataTypes {

	public static void main(String[] args) {
		
		
		//Primitive DataTypes : char, byte ,int , long , short ,float ,double , boolean
		//Non-Primitive DataTypes : String
		
//		Declaring an char - 16 bits
		// A character should be declared within single quotes
		// Value ranges from o to 65535
		char a = '$';
		System.out.println("Value of Character:"+" "+a);
		
		
//		Declaring an byte - 8bit
		// Value ranges from -128 to 127
		byte b = 26;
		System.out.println("Value of byte :"+" "+b);		
		
		
//		Declaring an integer - 32bit
		// Value ranges from -2e31 to -2e31-1	
		int c = 06;
		System.out.println("Value of integer :"+" "+c);		
		
		
//		Declaring an short - 16bit
		// Value ranges from -32768 to 32767
		short d =1997;
		System.out.println("Value of short :"+" "+d);		
		
		
//		Declaring an long - 64bit
		// 10digit value followed by l or L
		// Value ranges from -2e63 to -2e63-1
		long e = 7010309781L;
		System.out.println("Value of long :"+" "+e);
		
		
		
		
//		Declaring an float - 32bit
		// Decimal value followed by f or F
		// Value ranges from 1.4e-45 to 3.4e+38
		float f =2628.34233445567895f;
		System.out.println("Value of float :"+" "+f);
		
		
		
//		Declaring an double - 64bit
		//double can have a higher value after decimal point
		// Decimal value followed by d or D or can be passed without using any of it
		// Value ranges from 4.9e-324 to 1.8e+308
		double def = 2670923.234589937594839;
		System.out.println("Value of double1:"+" "+def);
		double g = 2670233.2345672356654d;
		System.out.println("Value of double :"+" "+g);
		
		
		
//		Declaring an boolean -NA
		// Values are True and False
		boolean h = true;
		System.out.println("Value of boolean :"+" "+h);
		
		
//Non - Primitive DataType
		
//      Declaring a String
       //String has to be Declared within ""
		
		String i = "Naveen";
		System.out.println("Value of String :"+" "+i);
		
		//String can also be Declared as follow
		
		String j = new String("Naveen");
		System.err.println("Value of String1 :"+" "+j);
		
		

		
		
		
		
		

		
		
		
		
		

	}

}
